export default function() {

    const randomPuzzle = puzzles[Math.floor(Math.random() * puzzles.length)]
    let k = 0

    const cells: cellInterface = {}
    for(let i = 1; i <= 9; i++){
        for(let j = 1; j <= 9; j++){
            let newCell = i + '_' + j
            cells[newCell] = /[1-9]/.test(randomPuzzle[k]) ? randomPuzzle[k] : ''
            k += 1
        }
    }

    return cells
}


